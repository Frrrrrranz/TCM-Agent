def seed_herbs(db: Database) -> int:
    """导入中药种子数据。"""
    count = 0
    for herb_data in HERBS_DATA:
        try:
            db.insert_herb(herb_data)
            count += 1
        except Exception as exc:
            logger.warning("导入中药 '%s' 失败: %s", herb_data["name"], exc)
    return count


if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    db_path = Path(__file__).resolve().parent.parent.parent / "data" / "tcm.db"
    db = Database(db_path)
    db.connect()
    count = seed_herbs(db)
    logger.info("中药种子数据导入完成，共 %d 条", count)
